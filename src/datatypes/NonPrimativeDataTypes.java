package datatypes;

public class NonPrimativeDataTypes {
	Byte a;
	Short b;
	Integer c;
	Long d;
	Float e;
	Double f;
	Character g;
	Boolean h;


	public static void main(String[] args) {
		NonPrimativeDataTypes sai=new NonPrimativeDataTypes();
        System.out.println(sai.a);
        System.out.println(sai.b);
        System.out.println(sai.c);
        System.out.println(sai.d);
        System.out.println(sai.e);
        System.out.println(sai.f);
        System.out.println(sai.g);
        System.out.println(sai.h);
	}

}
