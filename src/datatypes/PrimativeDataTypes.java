package datatypes;

public class PrimativeDataTypes {
	byte a;
	short b;
	int c;
	long d;
	float e;
	double f;
	char g;
	boolean h;

	public static void main(String[] args) {
		 PrimativeDataTypes sai=new  PrimativeDataTypes();
		 System.out.println( sai.a);
		 System.out.println( sai.b);
		 System.out.println( sai.c);
		 System.out.println( sai.d);
		 System.out.println( sai.e);
		 System.out.println( sai.f);
		 System.out.println( sai.g);
		 System.out.println( sai.h);

	}

}
